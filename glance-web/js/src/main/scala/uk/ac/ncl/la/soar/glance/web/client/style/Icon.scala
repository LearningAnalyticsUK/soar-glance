object Icon {

  /** ADT enumerating icon sizes */
  sealed trait IconSize
  case object Small extends IconSize
  case object Medium extends IconSize
  case object Large extends IconSize

  type Icon = VdomElement
  def apply(name: String)(size: IconSize = Small): Icon = {

    val sizeProp = size match {
      case Small => "fa-lg"
      case Medium => "fa-2x"
      case Large => "fa-3x"
    }
    <.i(^.className := s"fa fa-$name $sizeProp pull-left")
  }

